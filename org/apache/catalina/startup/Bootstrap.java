    /**
     * Main method and entry point when starting Tomcat via the provided
     * scripts.
     *
     * @param args Command line arguments to be processed
     */
    public static void main(String args[]) {
        //这里的daemon就是bootstrap本身，为啥要在这里检测daemon的值？跟单例有无关系？这是启动的脚本，也是关闭的脚本，用于检测是否启动
        System.out.println("begin yihau!!!");
        if (daemon == null) {
            // Don't set daemon until init() has completed
            //调用过bootstrap类的init方法后，在对daemon赋值，这里先新建一个Bootstrap对象。
            Bootstrap bootstrap = new Bootstrap();
            try {
                bootstrap.init();
            } catch (Throwable t) {
                handleThrowable(t);
                t.printStackTrace();
                return;
            }
            //调用过bootstrap类的init方法后，将其赋值给daemon
            daemon = bootstrap;
        } else {
            // When running as a service the call to stop will be on a new
            // thread so make sure the correct class loader is used to prevent
            // a range of class not found exceptions.
            //如果daemon不为null，说明服务正在运行，此时是在一个新的线程上调用stop命令，需要设置正确的线程上下文类
            //加载器，防止找不到有些类。
            Thread.currentThread().setContextClassLoader(daemon.catalinaLoader);
        }

        try {
            String command = "start";
            if (args.length > 0) {
                command = args[args.length - 1];
            }

            if (command.equals("startd")) {
                args[args.length - 1] = "start";
                daemon.load(args);
                daemon.start();
            } else if (command.equals("stopd")) {
                args[args.length - 1] = "stop";
                daemon.stop();
            } else if (command.equals("start")) {
                daemon.setAwait(true);//设置catalinaDaemon的阻塞状态
                daemon.load(args);//配置catalinaDaemon设置
                daemon.start();//开启catalinaDaemon服务
                if (null == daemon.getServer()) {
                    System.exit(1);//未成功开启退出
                }
            } else if (command.equals("stop")) {
                daemon.stopServer(args);
            } else if (command.equals("configtest")) {
                daemon.load(args);
                if (null == daemon.getServer()) {
                    System.exit(1);
                }
                System.exit(0);
            } else {
                log.warn("Bootstrap: command \"" + command + "\" does not exist.");
            }
        } catch (Throwable t) {
            // Unwrap the Exception for clearer error reporting
            if (t instanceof InvocationTargetException &&
                    t.getCause() != null) {
                t = t.getCause();
            }
            handleThrowable(t);
            t.printStackTrace();
            System.exit(1);
        }

    }


